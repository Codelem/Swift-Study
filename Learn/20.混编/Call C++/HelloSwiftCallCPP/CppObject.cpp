int CppObject::add(int a, int b){
    return a+b;
}

int CppObject::sub(int a, int b){
    return a-b;
}

int CppObject::static_add(int a, int b){
    return a+b;
}

int CppObject::static_sub(int a, int b){
    return a-b;
}
